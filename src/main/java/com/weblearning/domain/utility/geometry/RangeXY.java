package com.weblearning.domain.utility.geometry;

public class RangeXY {
	
	double lowerX;
	double upperX;
	double lowerY;
	double upperY;
	
	
	public double getLowerX() {
		return lowerX;
	}
	public void setLowerX(double lowerX) {
		this.lowerX = lowerX;
	}
	public double getUpperX() {
		return upperX;
	}
	public void setUpperX(double upperX) {
		this.upperX = upperX;
	}
	public double getLowerY() {
		return lowerY;
	}
	public void setLowerY(double lowerY) {
		this.lowerY = lowerY;
	}
	public double getUpperY() {
		return upperY;
	}
	public void setUpperY(double upperY) {
		this.upperY = upperY;
	}

}
